package org.example;

public class Func {

    public int valorSoma(int n1, int n2){
        return n1 + n2;
    }


    public int valorSubtracao(int n1, int n2){
        return n1 - n2;
    }


    public int valorDivisao(int n1, int n2){
        return n1 / n2;
    }


    public int valorMultiplicacao(int n1, int n2){
        return n1 * n2;
    }
}
